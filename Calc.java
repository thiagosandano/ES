public class Calc {

    
    public static float soma(float n1, float n2) {
        return n1 + n2;
    }
    public static float subtracao(float n1, float n2) {
        return n1 - n2;
    }
      public static float multiplicacao(float n1, float n2) {
        return n1 * n2;
    }
      public static float divisao(float n1, float n2) {
        return n1 / n2;
    }
    
}
